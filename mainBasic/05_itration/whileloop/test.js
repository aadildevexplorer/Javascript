// while loop

let index = 0;
while (index <= 10) {
  console.log(`Value is Index ${index}`);
  index = index + 2;
}

let myArr = ["flat", "batman", "superHero"];

let arr = 0;
while (arr < myArr.length) {
  console.log(`Value is ${myArr[arr]}`);

  arr = arr + 1;
}

// do while loop

let score = 1;
do {
  console.log(`score is ${score}`);
  score++;
} while (score <= 10);
